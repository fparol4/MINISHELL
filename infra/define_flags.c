#include "infra.h"

void	preencher_array(t_flags *flags)
{
	int	i;

	i = 0;
	while (flags->flags[i])
	{
		if ((i >= 'a' && i <= 'z') || (i >= 'A' && i <= 'Z'))
			flags->flags[i] |= CHAR_SKIP;
	}
}
